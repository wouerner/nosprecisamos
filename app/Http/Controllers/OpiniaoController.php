     */
    public function store(Request $request)
    {
        $opiniao = new Opiniao();

        $opiniao->projetos_id = $request->projetos_id;
        $opiniao->aprovado = $request->aprovado;
        $opiniao->users_id = Auth::user()->id;

        $sucesso = $opiniao->save();

        return ['success' => $sucesso];
    }

